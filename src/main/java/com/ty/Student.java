package com.ty;

public class Student {

	private int id;
	private String name;
	private String prantesName;
	private String teacherName;
	private int fees;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPrantesName() {
		return prantesName;
	}
	public void setPrantesName(String prantesName) {
		this.prantesName = prantesName;
	}
	public String getTeacherName() {
		return teacherName;
	}
	public void setTeacherName(String teacherName) {
		this.teacherName = teacherName;
	}
	public int getFees() {
		return fees;
	}
	public void setFees(int fees) {
		this.fees = fees;
	}
	
	
}
